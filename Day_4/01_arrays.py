import numpy as np

# Creating arrays

a = np.array([1, 2, 3, 4, 5])

zeros = np.zeros(5)

ones = np.ones(5)

arange = np.arange(1, 11)

linspace = np.linspace(0, 100, 6)

print("Array:")
print(a)

print("\nZeros:")
print(zeros)

print("\nOnes:")
print(ones)

print("\nArange:")
print(arange)

print("\nLinspace:")
print(linspace)